def project_state(oid):
    st = {}
    try:
        with open(PARTS_DB, "r", encoding="utf-8-sig", errors="replace") as fh:
            db = json.load(fh)
        rec = next((r for r in db if r.get("Name") == oid or
                    r.get("ObjectID", "").lstrip("^") == oid), None)
        if rec:
            st["parts_db"] = {k: rec.get(k) for k in
                              ("Name", "DisplayName", "Category", "SubCategory",
                               "ModelPath", "VariantOf")}
            mp = rec.get("ModelPath") or ""
            asset = mp.split("/")[-1].split(".")[0] if mp else ""
            if asset:
                st["mesh_asset"] = asset
                st["uasset_exists"] = os.path.isfile(
                    os.path.join(GAMEMESHES, asset + ".uasset"))
                st["obj_exists"] = os.path.isfile(
                    os.path.join(MESHSRC, asset + ".obj"))
    except Exception as e:
        st["parts_db_error"] = str(e)
    try:
        with open(PART_SLOTS, "r", encoding="utf-8-sig", errors="replace") as fh:
            slots = json.load(fh)
        if oid in slots:
            st["part_slots"] = {
                "n": len(slots[oid]),
                "slots": [{"mat": s.get("mat"), "tex": s.get("tex"),
                           "type": s.get("type", "lit")} for s in slots[oid]],
            }
    except Exception as e:
        st["part_slots_error"] = str(e)
    try:
        if "mesh_asset" in st and os.path.isfile(VERIFIED_MESHES):
            with open(VERIFIED_MESHES, "r", encoding="utf-8-sig") as fh:
                ver = json.load(fh)
            rel = "Content/NMSBaseBuilder/GameMeshes/%s.uasset" % st["mesh_asset"]
            st["mesh_verified"] = any(rel.lower() == k.replace("\\", "/").lower()
                                      for k in ver)
    except Exception:
        pass
    return st


