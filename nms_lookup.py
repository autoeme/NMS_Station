def et_props(el):
    """Дочерние <Property> по name."""
    d = {}
    for c in el:
        d.setdefault(c.get("name"), []).append(c)
    return d


def node_info(el):
    p = et_props(el)
    name = p.get("Name", [None])[0]
    typ = p.get("Type", [None])[0]
    info = {
        "Name": name.get("value") if name is not None else "",
        "Type": typ.get("value") if typ is not None else "",
        "Attrs": {},
        "Transform": {},
        "Children": [],
    }
    tr = p.get("Transform", [None])[0]
    if tr is not None:
        for c in tr:
            info["Transform"][c.get("name")] = c.get("value")
    attrs = p.get("Attributes", [None])[0]
    if attrs is not None:
        for a in attrs:
            ap = et_props(a)
            an = ap.get("Name", [None])[0]
            av = ap.get("Value", [None])[0]
            if an is not None and av is not None:
                info["Attrs"][an.get("value")] = av.get("value")
    ch = p.get("Children", [None])[0]
    if ch is not None:
        for c in ch:
            info["Children"].append(node_info(c))
    return info


def walk_nodes(node, out=None):
    if out is None:
        out = []
    out.append(node)
    for c in node["Children"]:
        walk_nodes(c, out)
    return out


def parse_scene(scene_file):
    tree = ET.parse(scene_file)
    root_prop = tree.getroot()  # <Data>
    root = node_info(root_prop)
    nodes = walk_nodes(root)
    meshes, locators, refs, colls = [], [], [], []
    for n in nodes:
        t = n["Type"]
        if t == "MESH":
            lod = n["Attrs"].get("LODLEVEL", "")
            meshes.append({
                "Name": n["Name"],
                "MATERIAL": n["Attrs"].get("MATERIAL", ""),
                "LODLEVEL": lod,
            })
        elif t == "LOCATOR":
            tr = n["Transform"]
            locators.append({
                "Name": n["Name"],
                "pos": [tr.get("TransX"), tr.get("TransY"), tr.get("TransZ")],
                "rotY": tr.get("RotY"),
            })
        elif t == "REFERENCE":
            refs.append({"Name": n["Name"],
                         "SCENEGRAPH": n["Attrs"].get("SCENEGRAPH", "")})
        elif t == "COLLISION":
            colls.append({"Name": n["Name"],
                          "TYPE": n["Attrs"].get("TYPE", "")})
    return {
        "file": scene_file,
        "GEOMETRY": root["Attrs"].get("GEOMETRY", ""),
        "NUMLODS": root["Attrs"].get("NUMLODS", ""),
        "meshes": meshes,
        "locators": locators,
        "references": refs,
        "collisions": colls,
    }


# ---------------------------------------------------------------- эмуляция шейдера
# Числовой «портрет пикселя» материала — что убершейдер игры сделает с этим материалом.
# Формулы 1:1 из проверенной заметки 6_LOGIC_NOTES\ИССЛЕДОВАНИЕ_ДЕТАЛИ_2026-07\10_ATLAS_KRASKA
# (frag 22517998153629701, адверсарно сверено на 4337 материалах). Точнее симулятора
# NMS_АТЛАС_ИСТИНА: несёт Subsurface (masks.A·Params.y) и правку применения AO (лерп ambient).

# бит gDynamicFlags.x -> активная ветка. ПРОВЕРЕНО ПРЯМО ПО ИГРОВОМУ ШЕЙДЕРУ
# SHADERS_FULL\...\ubershader_frag_lit_defer_22517998153629701.glsl (декомпиляция .spv игры):
#   бит 1 → _1940 (стр.253), 2 → _1943 (269), 4 → _1947 (285), 8 → _1949 (301),
#   16 → _1942/subsurface (262), 256 → _1930/покраска (214), 8192 → _1948/glow (292).
DYNBITS = {
    1:    "Roughness = (1−masks.R)·Params.x  (иначе 1·Params.x)  [_1940]",
    2:    "Metallic = masks.G  (иначе Params.z)  [_1943]",
    4:    "spec/scatter G-буфер.w = masks.G·Params.w  (иначе 0)  [_1947]",
    8:    "Glow-маска = masks.B  (иначе 1)  [_1949]",
    16:   "Subsurface из masks.A  (иначе 1·Params.y)  [_1942]",
    64:   "AO = gOcclusionMap.G",
    256:  "★ покраска: zone = 4−int(cmask.R·4+0.5), albedo·=свотч[zone]  [_1930]",
    2048: "AO ограничен вершинным цветом.w (min)",
    8192: "Glow-сила = Params2.x  [_1948]",
}


def _f(vals, i, default=0.0):
    try:
        return float(vals[i])
    except (TypeError, ValueError, IndexError):
        return default


def emulate_shader(flags, uniforms):
    """Возвращает dict: активные биты + формулы Roughness/Metallic/Glow/Subsurface/AO/zone
    с подставленными числами материала. Всё 1:1 из GLSL (см. заметку 10)."""
    fl = set(flags or [])
    dynx = 0
    dv = uniforms.get("gDynamicFlags")
    if dv:
        try:
            dynx = int(float(dv[0])) if dv[0] not in (None, "") else 0
        except (TypeError, ValueError):
            dynx = 0
    P = uniforms.get("gMaterialParamsVec4") or []
    P2 = uniforms.get("gMaterialParams2Vec4") or []
    px, py, pz = _f(P, 0, 0.9), _f(P, 1, 0.5), _f(P, 2, 0.0)
    out = {
        "dyn_x": dynx,
        "active_bits": [(b, d) for b, d in sorted(DYNBITS.items()) if dynx & b],
        "roughness": ("clamp((1−masks.R)·%.2f)" % px) if (dynx & 1) else ("clamp(1·%.2f)=%.2f" % (px, min(px, 1.0))),
        "metallic": "masks.G" if (dynx & 2) else ("Params.z=%.2f" % pz),
    }
    if dynx & 8192:
        gmask = "masks.B" if (dynx & 8) else "1"
        out["glow"] = "clamp(%s·Params2.x=%.2f, 0..4)" % (gmask, _f(P2, 0, 0.0))
    else:
        out["glow"] = "0 (выключено)"
    # Subsurface — наша ключевая находка: masks.A НЕ AO, а подповерхностное свечение.
    # Гейт — ТОЧНЫЙ игровой бит 16 (не «есть _F25»): shader _1942 = (dyn&16?masks.A:1)·Params.y
    out["subsurface"] = ("masks.A·Params.y=%.2f" % py) if (dynx & 16) else ("1·Params.y=%.2f (masks.A-ветка выкл)" % py)
    out["ao"] = "gOcclusionMap.G" + ("  (min с вершинным.w)" if (dynx & 2048) else "") if (dynx & 64) else "1.0 (нет AO-карты)"
    if dynx & 256:
        out["paint"] = "zone = 4−int(cmask.R·4+0.5); zone<4 → albedo·=свотч палитры[zone] (без своей cmask: R=126 → зона 2 целиком)"
    else:
        out["paint"] = "не красится (бит 256 выкл)"
    out["is_multitexture"] = "_F55_MULTITEXTURE" in fl
    out["is_glass"] = "_F30_REFRACTION" in fl
    out["is_unlit"] = "_F07_UNLIT" in fl
    out["uv2"] = ("_F16_DIFFUSE2MAP" in fl) or ("_F42_DETAIL_NORMAL" in fl)
    return out


def parse_material(mat_path_game):
    """Материал по игровому пути: полное имя (MATERIALS/NEW_EXTRACT) ->
    короткое имя (META_NEW, с предупреждением о коллизиях)."""
    hits = find_decoded(mat_path_game, MAT_DIRS_FULLNAME, ".material.MXML")
    approx = False
    if not hits:
        short = norm_key(mat_path_game).split("__")[-1]
        for d in MAT_DIRS_SHORTNAME:
            for f in list_dir(d):
                b = os.path.basename(f)
                if b.lower() == short + ".material.mxml":
                    hits.append(f)
                    approx = True
    if not hits:
        return {"game_path": mat_path_game, "decoded": None}
    f = hits[0]
    try:
        tree = ET.parse(f)
    except ET.ParseError:
        return {"game_path": mat_path_game, "decoded": f, "error": "parse"}
    txt_flags, samplers = [], []
    mclass = ""
    colour = None
    uniforms = {}   # имя юниформы -> [X,Y,Z,W] (Float + UInt: Params/Params2/DynamicFlags)
    root = tree.getroot()

    def _xyzw(el):
        # X/Y/Z/W лежат под под-свойством "Values" (не прямые дети юниформы)
        p = et_props(el)
        holder = p["Values"][0] if "Values" in p else el
        vp = et_props(holder)
        return [vp[k][0].get("value") if k in vp else None for k in ("X", "Y", "Z", "W")]

    def rec(el):
        nonlocal mclass, colour
        name = el.get("name")
        val = el.get("value") or ""
        if name == "MaterialFlag":
            txt_flags.append(el.get("value"))
        elif name == "MaterialClass":
            mclass = el.get("value")
        elif name == "Samplers" and val == "TkMaterialSampler":
            p = et_props(el)
            samplers.append({
                "Name": p["Name"][0].get("value") if "Name" in p else "",
                "Map": p["Map"][0].get("value") if "Map" in p else "",
                "IsSRGB": p["IsSRGB"][0].get("value") if "IsSRGB" in p else "",
            })
        elif name in ("Uniforms_Float", "Uniforms_UInt") and val.startswith("TkMaterialUniform"):
            p = et_props(el)
            un = p["Name"][0].get("value") if "Name" in p else ""
            if un:
                uniforms[un] = _xyzw(el)
                if un == "gMaterialColourVec4":
                    colour = uniforms[un]
        for c in el:
            rec(c)

    rec(root)
    return {
        "game_path": mat_path_game,
        "decoded": f,
        "approx_by_name": approx,
        "Class": mclass,
        "Flags": txt_flags,
        "gMaterialColourVec4": colour,
        "Uniforms": uniforms,
        "Shader": emulate_shader(txt_flags, uniforms),
        "Samplers": samplers,
    }


def find_geometry(geo_path_game):
    hits = find_decoded(geo_path_game, GEO_DIRS, ".geometry.data.MXML", with_quality=True)
    out = []
    for f, q in hits:
        out.append({"file": f, "size": os.path.getsize(f), "match": q})
    # точное совпадение полного пути всегда выше; внутри — большее лучше
    out.sort(key=lambda x: (0 if x["match"] == "exact" else 1, -x["size"]))
    return out

