def parse_objectstable(oid):
    entry = find_table_entry(
        OBJECTSTABLE, r'_id="%s"' % re.escape(oid),
        '\t\t<Property name="Objects"')
    if not entry:
        return None
    groups = re.findall(
        r'<Property name="Group" value="([^"]*)" />\s*\n\s*'
        r'<Property name="SubGroupName" value="([^"]*)"', entry)
    style_m = re.search(
        r'name="Style" value="GcBaseBuildingPartStyle">\s*\n\s*'
        r'<Property name="Style" value="([^"]*)"', entry)
    comp_block = re.search(
        r'<Property name="CompositePartObjectIDs">(.*?)^\t{3}</Property>',
        entry, re.S | re.M)
    composites = prop_vals(comp_block.group(1), "CompositePartObjectIDs") if comp_block else []
    fam_block = re.search(
        r'<Property name="FamilyIDs">(.*?)^\t{3}</Property>', entry, re.S | re.M)
    families = prop_vals(fam_block.group(1), "FamilyIDs") if fam_block else []
    return {
        "ID": prop_val(entry, "ID"),
        "Style": style_m.group(1) if style_m else "",
        "PlacementScene": prop_val(entry, "Filename"),
        "SinglePartID": prop_val(entry, "SinglePartID"),
        "IsPlaceable": prop_val(entry, "IsPlaceable"),
        "ShowInBuildMenu": prop_val(entry, "ShowInBuildMenu"),
        "Groups": ["%s / %s" % g for g in groups],
        "ColourPaletteGroupId": prop_val(entry, "ColourPaletteGroupId"),
        "MaterialGroupId": prop_val(entry, "MaterialGroupId"),
        "CanChangeColour": prop_val(entry, "CanChangeColour"),
        "CanChangeMaterial": prop_val(entry, "CanChangeMaterial"),
        "CanScale": prop_val(entry, "CanScale"),
        "CanRotate3D": prop_val(entry, "CanRotate3D"),
        "SnapRotateBlocked": prop_val(entry, "SnapRotateBlocked"),
        "Buildable": {k: prop_val(entry, k) for k in (
            "BuildableOnPlanetBase", "BuildableOnSpaceBase", "BuildableOnFreighter",
            "BuildableUnderwater", "BuildableOnPlanet")},
        "CompositePartObjectIDs": composites,
        "FamilyIDs": families,
        "HasLinkGridData": 'name="LinkGridData"' in entry,
    }


def parse_partstable(pid):
    entry = find_table_entry(
        PARTSTABLE, r'<Property name="ID" value="%s" />' % re.escape(pid),
        '\t\t<Property name="Table"')
    if entry is None:
        # формат открытия записи мог отличаться — пробуем универсально по любому 2-таб открытию
        entry = find_table_entry(
            PARTSTABLE, r'<Property name="ID" value="%s" />' % re.escape(pid),
            '\t\t<Property ')
    if not entry:
        return None
    styles = re.findall(
        r'<Property name="Style" value="([^"]*)" />\s*\n\s*</Property>\s*\n\s*'
        r'<Property name="Model" value="TkModelResource">\s*\n\s*'
        r'<Property name="Filename" value="([^"]*)"', entry)
    return {"ID": pid, "StyleModels": [{"Style": s, "Scene": f} for s, f in styles]}


def parse_legacy(oid):
    if not os.path.isfile(LEGACYTABLE):
        return None
    entry = find_table_entry(
        LEGACYTABLE, r'value="%s"' % re.escape(oid), '\t\t<Property ')
    return {"found": bool(entry)} if entry else None


def hull_bbox(pid, style=None):
    """MagicData-хуллы (мировой эталон): все стили, bbox каждого."""
    entries = find_table_entries(
        AUTOGEN, r'<Property name="PartID" value="%s" />' % re.escape(pid),
        '\t\t<Property name="PartsData"')
    out = []
    for e in entries:
        st = ""
        m = re.search(r'name="Style" value="GcBaseBuildingPartStyle">\s*\n\s*'
                      r'<Property name="Style" value="([^"]*)"', e)
        if m:
            st = m.group(1)
        xs = [float(v) for v in prop_vals(e, "X")]
        ys = [float(v) for v in prop_vals(e, "Y")]
        zs = [float(v) for v in prop_vals(e, "Z")]
        if not xs:
            continue
        out.append({
            "Style": st,
            "min": [min(xs), min(ys), min(zs)],
            "max": [max(xs), max(ys), max(zs)],
            "size": [max(xs) - min(xs), max(ys) - min(ys), max(zs) - min(zs)],
            "nverts": len(xs),
        })
    if style:
        pref = [h for h in out if h["Style"].lower() == style.lower()]
        if pref:
            return pref + [h for h in out if h not in pref]
    return out

