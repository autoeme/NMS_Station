def part_id_candidates(oid, obj):
    cands = []
    if obj and obj.get("SinglePartID"):
        cands.append(obj["SinglePartID"])
    if oid.startswith("_"):
        cands.append(oid)
    m = re.match(r"^[A-Z]_(.+)$", oid)
    if m:
        cands.append("_" + m.group(1))
    cands.append("_" + oid)
    seen, out = set(), []
    for c in cands:
        if c and c not in seen:
            seen.add(c)
            out.append(c)
    return out


def lookup(oid, style=None, brief=False, use_placement=False):
    r = {"query": oid}
    obj = parse_objectstable(oid)
    r["objectstable"] = obj
    if obj and not style and obj.get("Style") and obj["Style"] != "None":
        style = obj["Style"]
    r["style_used"] = style

    part = None
    for pid in part_id_candidates(oid, obj):
        part = parse_partstable(pid)
        if part:
            break
    r["partstable"] = part
    r["legacy"] = parse_legacy(oid)

    # какая сцена главная: partstable[style] > PlacementScene
    scene_path = None
    if part and part["StyleModels"]:
        if style:
            hit = next((s for s in part["StyleModels"]
                        if s["Style"].lower() == style.lower()), None)
            scene_path = hit["Scene"] if hit else part["StyleModels"][0]["Scene"]
        else:
            scene_path = part["StyleModels"][0]["Scene"]
    elif obj and obj.get("PlacementScene"):
        scene_path = obj["PlacementScene"]

    # ЛОВУШКА S_CONTAINER0: partstable-сцена и PlacementScene бывают РАЗНЫМИ мешами
    # (container_0-ящик vs storageroom-комната). Сверять с иконкой (Правило 18)!
    if part and obj and obj.get("PlacementScene") and scene_path:
        a = re.sub(r"_placement$", "", norm_key(scene_path).split("__")[-1])
        b = re.sub(r"_placement$", "", norm_key(obj["PlacementScene"]).split("__")[-1])
        if a != b:
            r["scene_mismatch"] = {"partstable": scene_path,
                                   "placement": obj["PlacementScene"]}
            if use_placement:
                scene_path = obj["PlacementScene"]
    r["scene_path"] = scene_path

    if scene_path and not brief:
        files = find_decoded(scene_path, SCENE_DIRS, ".scene.MXML")
        if files:
            scene = parse_scene(files[0])
            r["scene"] = scene
            if scene["GEOMETRY"]:
                r["geometry"] = find_geometry(scene["GEOMETRY"])
            mats = sorted({m["MATERIAL"] for m in scene["meshes"]
                           if m["MATERIAL"]})
            r["materials"] = [parse_material(mp) for mp in mats]
        else:
            r["scene"] = {"error": "сцена не расшифрована: " + scene_path}

    # хулл: по part-ID (в autogen ключи part-ID) и по самому oid
    hulls = []
    for pid in part_id_candidates(oid, obj):
        hulls = hull_bbox(pid, style)
        if hulls:
            r["hull_part_id"] = pid
            break
    if not hulls:
        hulls = hull_bbox(oid, style)
        if hulls:
            r["hull_part_id"] = oid
    r["hulls"] = hulls

    r["icon"] = icon_info(oid)
    r["project"] = project_state(oid)
    return r


