def prop_val(text, name, default=""):
    """Первое <Property name="NAME" value="..."/> в куске текста."""
    m = re.search(r'<Property name="%s" value="([^"]*)"' % re.escape(name), text)
    return m.group(1) if m else default


def prop_vals(text, name):
    return re.findall(r'<Property name="%s" value="([^"]*)"' % re.escape(name), text)


def norm_key(game_path):
    """MODELS\\A\\B\\C.SCENE.MBIN -> models__a__b__c (без расширения)."""
    p = game_path.replace("\\", "/").lower()
    p = re.sub(r"\.(scene|geometry|material)\.mbin$", "", p)
    p = re.sub(r"\.(scene|geometry|material)$", "", p)
    return p.strip("/").replace("/", "__")


def suffix_match(full_key, fname_base):
    """Совпадение на границе '__': короткое имя = хвост полного пути."""
    if full_key == fname_base:
        return True
    if full_key.endswith("__" + fname_base):
        return True
    if fname_base.endswith("__" + full_key):
        return True
    return False


_dir_cache = {}

def list_dir(d, recursive=False):
    if d in _dir_cache:
        return _dir_cache[d]
    out = []
    if os.path.isdir(d):
        if recursive:
            for root, _dirs, files in os.walk(d):
                for f in files:
                    out.append(os.path.join(root, f))
        else:
            out = [os.path.join(d, f) for f in os.listdir(d)]
    _dir_cache[d] = out
    return out


def find_decoded(game_path, dirs, ext, with_quality=False):
    """Ищет расшифрованный файл (.scene.MXML / .geometry.data.MXML / .material.MXML)
    по игровому пути во всех папках; сверка по ПОЛНОМУ пути на границе '__'.
    Для папок с реальной структурой (MESHCOMMON_FULL) ключ = относительный путь,
    а не голое имя — иначе чужие одноимённые меши (teleporter!) дают ложный матч.
    quality: 'exact' = полный путь совпал, 'suffix' = совпал только хвост имени."""
    key = norm_key(game_path)
    hits = []
    for d in dirs:
        recursive = d.endswith("MESHCOMMON_FULL")
        for f in list_dir(d, recursive=recursive):
            base = os.path.basename(f)
            if not base.endswith(ext):
                continue
            if recursive:
                rel = os.path.relpath(f, d)
                fb = rel[: -len(ext)].lower().replace("\\", "__").replace("/", "__")
            else:
                fb = base[: -len(ext)].lower()
            if not suffix_match(key, fb):
                continue
            exact = (fb == key or fb.endswith("__" + key))
            hits.append((f, "exact" if exact else "suffix"))
    if with_quality:
        return hits
    return [f for f, _q in hits]


def find_table_entry(path, id_regex, open_prefix):
    """Стриминг по таблице: копим текущую запись (открытие на open_prefix),
    возвращаем первую запись, где встретился id_regex."""
    rx = re.compile(id_regex)
    close_line = open_prefix.split("<")[0] + "</Property>"
    entry = []
    inside = False
    hit = False
    with open(path, "r", encoding="utf-8", errors="replace") as fh:
        for line in fh:
            ls = line.rstrip("\n")
            if ls.startswith(open_prefix):
                entry = [ls]
                inside = True
                hit = bool(rx.search(ls))
                continue
            if inside:
                entry.append(ls)
                if not hit and rx.search(ls):
                    hit = True
                if ls == close_line:
                    if hit:
                        return "\n".join(entry)
                    inside = False
                    entry = []
    return None


def find_table_entries(path, id_regex, open_prefix, limit=8):
    """Как выше, но все записи (например хуллы по всем стилям)."""
    rx = re.compile(id_regex)
    close_line = open_prefix.split("<")[0] + "</Property>"
    out = []
    entry = []
    inside = False
    hit = False
    with open(path, "r", encoding="utf-8", errors="replace") as fh:
        for line in fh:
            ls = line.rstrip("\n")
            if ls.startswith(open_prefix):
                entry = [ls]
                inside = True
                hit = bool(rx.search(ls))
                continue
            if inside:
                entry.append(ls)
                if not hit and rx.search(ls):
                    hit = True
                if ls == close_line:
                    if hit:
                        out.append("\n".join(entry))
                        if len(out) >= limit:
                            return out
                    inside = False
                    entry = []
    return out

