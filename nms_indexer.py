def et_props(el):
    d = {}
    for c in el:
        d.setdefault(c.get("name"), []).append(c)
    return d


def node_info(el):
    p = et_props(el)
    g = lambda k: (p.get(k, [None])[0].get("value") if p.get(k, [None])[0] is not None else "")
    info = {"Name": g("Name"), "Type": g("Type"), "Attrs": {}, "Transform": {}, "Children": []}
    tr = p.get("Transform", [None])[0]
    if tr is not None:
        for c in tr:
            info["Transform"][c.get("name")] = c.get("value")
    attrs = p.get("Attributes", [None])[0]
    if attrs is not None:
        for a in attrs:
            ap = et_props(a)
            an, av = ap.get("Name", [None])[0], ap.get("Value", [None])[0]
            if an is not None and av is not None:
                info["Attrs"][an.get("value")] = av.get("value")
    ch = p.get("Children", [None])[0]
    if ch is not None:
        for c in ch:
            info["Children"].append(node_info(c))
    return info


def walk(n, out=None):
    if out is None:
        out = []
    out.append(n)
    for c in n["Children"]:
        walk(c, out)
    return out


def parse_scene_mxml(mxml):
    try:
        root = node_info(ET.parse(mxml).getroot())
    except ET.ParseError as e:
        return {"error": "parse: %s" % e}
    nodes = walk(root)
    meshes, snap, refs, colls = [], [], [], []
    for n in nodes:
        t = n["Type"]
        if t == "MESH" and n["Attrs"].get("LODLEVEL", "0") == "0":
            meshes.append({"name": n["Name"], "material": n["Attrs"].get("MATERIAL", "")})
        elif t == "LOCATOR":
            nm = (n["Name"] or "").lower()
            if "snappoint" in nm or "_in_" in nm or "_out_" in nm:
                tr = n["Transform"]
                snap.append({"name": n["Name"],
                             "pos": [tr.get("TransX"), tr.get("TransY"), tr.get("TransZ")],
                             "rotY": tr.get("RotY")})
        elif t == "REFERENCE":
            refs.append({"name": n["Name"], "scene": n["Attrs"].get("SCENEGRAPH", "")})
        elif t == "COLLISION":
            colls.append({"name": n["Name"], "type": n["Attrs"].get("TYPE", "")})
    return {"geometry": root["Attrs"].get("GEOMETRY", ""),
            "numlods": root["Attrs"].get("NUMLODS", ""),
            "meshes": meshes, "snap": snap, "references": refs, "collisions": colls}


def parse_material_mxml(mxml):
    try:
        tree = ET.parse(mxml)
    except ET.ParseError as e:
        return {"error": "parse: %s" % e}
    flags, samplers, mclass, colour = [], [], "", None

    def rec(el):
        nonlocal mclass, colour
        name = el.get("name")
        if name == "MaterialFlag":
            flags.append(el.get("value"))
        elif name == "MaterialClass":
            mclass = el.get("value")
        elif name == "Samplers" and el.get("value") == "TkMaterialSampler":
            p = et_props(el)
            g = lambda k: (p.get(k, [None])[0].get("value") if p.get(k, [None])[0] is not None else "")
            samplers.append({"name": g("Name"), "map": g("Map"), "srgb": g("IsSRGB")})
        elif name == "Uniforms_Float" and el.get("value") == "TkMaterialUniform_Float":
            p = et_props(el)
            un = p.get("Name", [None])[0]
            if un is not None and un.get("value") == "gMaterialColourVec4":
                vals = re.findall(r'value="([-\d.]+)"', ET.tostring(el, encoding="unicode"))
                colour = vals[1:5] if len(vals) > 4 else vals
        for c in el:
            rec(c)

    rec(tree.getroot())
    return {"class": mclass, "flags": flags, "colour": colour, "samplers": samplers}


