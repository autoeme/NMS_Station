def prop_val(text, name, default=""):
    m = re.search(r'<Property name="%s" value="([^"]*)"' % re.escape(name), text)
    return m.group(1) if m else default


def prop_vals(text, name):
    return re.findall(r'<Property name="%s" value="([^"]*)"' % re.escape(name), text)


def iter_entries(path, open_prefix):
    """Стриминг записей таблицы (открытие на open_prefix, закрытие тем же отступом)."""
    close_line = open_prefix.split("<")[0] + "</Property>"
    entry, inside = [], False
    with open(path, "r", encoding="utf-8", errors="replace") as fh:
        for line in fh:
            ls = line.rstrip("\n")
            if ls.startswith(open_prefix):
                entry, inside = [ls], True
                continue
            if inside:
                entry.append(ls)
                if ls == close_line:
                    yield "\n".join(entry)
                    inside = False


def parse_objectstable_full(mxml):
    objs = {}
    for e in iter_entries(mxml, '\t\t<Property name="Objects"'):
        oid = prop_val(e, "ID")
        if not oid:
            continue
        style_m = re.search(r'name="Style" value="GcBaseBuildingPartStyle">\s*\n\s*'
                            r'<Property name="Style" value="([^"]*)"', e)
        groups = re.findall(r'<Property name="Group" value="([^"]*)" />\s*\n\s*'
                            r'<Property name="SubGroupName" value="([^"]*)"', e)
        comp = re.search(r'<Property name="CompositePartObjectIDs">(.*?)^\t{3}</Property>',
                         e, re.S | re.M)
        fam = re.search(r'<Property name="FamilyIDs">(.*?)^\t{3}</Property>', e, re.S | re.M)
        objs[oid] = {
            "Style": style_m.group(1) if style_m else "",
            "PlacementScene": prop_val(e, "Filename"),
            "SinglePartID": prop_val(e, "SinglePartID"),
            "Groups": ["%s/%s" % g for g in groups],
            "ShowInBuildMenu": prop_val(e, "ShowInBuildMenu") == "true",
            "IsPlaceable": prop_val(e, "IsPlaceable") == "true",
            "ColourPaletteGroupId": prop_val(e, "ColourPaletteGroupId"),
            "MaterialGroupId": prop_val(e, "MaterialGroupId"),
            "CanChangeColour": prop_val(e, "CanChangeColour") == "true",
            "CanChangeMaterial": prop_val(e, "CanChangeMaterial") == "true",
            "CanScale": prop_val(e, "CanScale") == "true",
            "CanRotate3D": prop_val(e, "CanRotate3D") == "true",
            "SnapRotateBlocked": prop_val(e, "SnapRotateBlocked") == "true",
            "Composites": prop_vals(comp.group(1), "CompositePartObjectIDs") if comp else [],
            "Families": prop_vals(fam.group(1), "FamilyIDs") if fam else [],
            "HasLinkGridData": 'name="LinkGridData"' in e,
            "IconOverrideProductID": prop_val(e, "IconOverrideProductID"),
        }
    return objs


def parse_product_icons(mxml):
    """nms_basepartproducts: ID продукта (= ID детали) -> путь DDS-иконки."""
    icons = {}
    for e in iter_entries(mxml, '\t\t<Property '):
        pid = prop_val(e, "ID")
        m = re.search(r'name="Icon" value="TkTextureResource">\s*\n\s*'
                      r'<Property name="Filename" value="([^"]*)"', e)
        if pid and m and m.group(1):
            icons[pid] = m.group(1)
    return icons


def parse_partstable_full(mxml):
    parts = {}
    # свежий декод 6.34 открывает записи как name="Parts"; старые дампы — name="Table"
    opener = '\t\t<Property name="Parts"'
    with open(mxml, "r", encoding="utf-8", errors="replace") as fh:
        head = fh.read(4096)
    if 'name="Parts"' not in head:
        opener = '\t\t<Property name="Table"'
    for e in iter_entries(mxml, opener):
        pid = prop_val(e, "ID")
        if not pid:
            continue
        styles = re.findall(
            r'<Property name="Style" value="([^"]*)" />\s*\n\s*</Property>\s*\n\s*'
            r'<Property name="Model" value="TkModelResource">\s*\n\s*'
            r'<Property name="Filename" value="([^"]*)"', e)
        parts[pid] = {s: f for s, f in styles}
    return parts


def parse_autogen_full(mxml):
    hulls = {}
    for e in iter_entries(mxml, '\t\t<Property name="PartsData"'):
        pid = prop_val(e, "PartID")
        if not pid:
            continue
        style_m = re.search(r'name="Style" value="GcBaseBuildingPartStyle">\s*\n\s*'
                            r'<Property name="Style" value="([^"]*)"', e)
        st = style_m.group(1) if style_m else "None"
        xs = [float(v) for v in prop_vals(e, "X")]
        ys = [float(v) for v in prop_vals(e, "Y")]
        zs = [float(v) for v in prop_vals(e, "Z")]
        if not xs:
            continue
        hulls.setdefault(pid, {})[st] = {
            "min": [round(min(xs), 4), round(min(ys), 4), round(min(zs), 4)],
            "max": [round(max(xs), 4), round(max(ys), 4), round(max(zs), 4)],
            "size": [round(max(xs) - min(xs), 4), round(max(ys) - min(ys), 4),
                     round(max(zs) - min(zs), 4)],
        }
    return hulls


def parse_legacy_ids(mxml):
    ids = set()
    for e in iter_entries(mxml, '\t\t<Property name="Objects"'):
        oid = prop_val(e, "ID")
        if oid:
            ids.add(oid)
    return ids


