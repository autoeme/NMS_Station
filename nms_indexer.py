def norm(p):
    return p.replace("\\", "/").lower()


def _long(p):
    """Windows: префикс \\\\?\\ снимает лимит пути 260 символов (глубокие папки)."""
    if os.name == "nt":
        p2 = os.path.abspath(p)
        if not p2.startswith("\\\\?\\"):
            return "\\\\?\\" + p2
    return p


def build_manifest(pcbanks, out_dir, rescan=False):
    """путь_в_игре(lower) -> {pak, index, name} (+ dups счётчик)."""
    mf_path = os.path.join(out_dir, "pak_manifest.json")
    if os.path.isfile(mf_path) and not rescan:
        with open(mf_path, "r", encoding="utf-8") as fh:
            print("[0] оглавление паков: из кэша pak_manifest.json")
            return json.load(fh)
    paks = sorted(f for f in os.listdir(pcbanks) if f.lower().endswith(".pak"))
    if not paks:
        sys.exit("В папке нет .pak файлов: " + pcbanks)
    manifest, dups = {}, 0
    for i, pk in enumerate(paks):
        full = os.path.join(pcbanks, pk)
        try:
            pak = HGPak(full)
            names = pak.names()
            pak.close()
        except Exception as e:
            print("  ! пропуск %s: %s" % (pk, e))
            continue
        prec = "precache" in pk.lower()
        for idx, nm in enumerate(names):
            key = norm(nm)
            if key in manifest:
                dups += 1
                # приоритет pak'у с precache в имени — там актуальные таблицы
                if not prec:
                    continue
            manifest[key] = {"pak": pk, "index": idx, "name": nm}
        print("  [%d/%d] %-40s файлов: %d" % (i + 1, len(paks), pk, len(names)))
    with open(mf_path, "w", encoding="utf-8") as fh:
        json.dump(manifest, fh, ensure_ascii=False)
    print("[0] всего путей: %d (перекрытий: %d) -> pak_manifest.json" % (len(manifest), dups))
    return manifest


