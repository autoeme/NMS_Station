_open_paks = {}

def get_pak(pcbanks, pk):
    if pk not in _open_paks:
        _open_paks[pk] = HGPak(os.path.join(pcbanks, pk))
    return _open_paks[pk]


def extract_files(pcbanks, manifest, game_paths, raw_dir):
    """Извлекает список игровых путей в raw_dir (со структурой). Возвращает
    {game_path_lower: локальный_путь | None}. Уже извлечённое не трогает."""
    out = {}
    for gp in game_paths:
        key = norm(gp)
        rec = manifest.get(key)
        if rec is None and not key.endswith(".pc"):
            rec = manifest.get(key + ".pc")   # геометрия в паках = .MBIN.PC
            key = key + ".pc" if rec else key
        if rec is None:
            out[norm(gp)] = None
            continue
        dst = os.path.join(raw_dir, key.replace("/", os.sep))
        if not os.path.isfile(_long(dst)):
            os.makedirs(_long(os.path.dirname(dst)), exist_ok=True)
            data = get_pak(pcbanks, rec["pak"]).extract(rec["index"])
            with open(_long(dst), "wb") as fh:
                fh.write(data)
        out[norm(gp)] = dst
    return out


def decode_files(mbin_exe, files):
    """MBINCompiler -y -q пачками; пропускает то, у чего .MXML уже есть.
    Возвращает {mbin_путь: mxml_путь | None}."""
    res, todo = {}, []
    for f in files:
        if f is None:
            continue
        mx = re.sub(r"\.mbin(\.pc)?$", ".MXML", f, flags=re.I)
        res[f] = mx if os.path.isfile(mx) else None
        if res[f] is None:
            todo.append(f)
    BATCH = 40
    for i in range(0, len(todo), BATCH):
        batch = todo[i:i + BATCH]
        subprocess.run([mbin_exe, "-y", "-q"] + batch,
                       capture_output=True, timeout=600)
        if len(todo) > BATCH:
            print("    декод: %d/%d" % (min(i + BATCH, len(todo)), len(todo)))
    for f in todo:
        mx = re.sub(r"\.mbin(\.pc)?$", ".MXML", f, flags=re.I)
        res[f] = mx if os.path.isfile(mx) else None
    return res


